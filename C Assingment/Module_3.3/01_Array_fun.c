#include<stdio.h>

    // wap to find out the max number from given number array using function.

int main()
{
    int i, max;

    int a[8] = {12, 13, 14, 15, 16, 17, 18, 186};
    max = a[0];
    for (i = 0; i < 8; i++)
    {
        if (max = a[i])
        {
            max = a[i];
        }
    }
    printf("Maximum value of array an %d\n", max);

    return 0;
}